-- HRMS Master Oracle Setup Script
-- Compatible with Oracle 19c / 21c / 23ai

-- 1. Users & Authentication
CREATE TABLE HRMS_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    ROLE VARCHAR2(20) DEFAULT 'staff',
    FULL_NAME VARCHAR2(100),
    STATUS VARCHAR2(20) DEFAULT 'Active',
    ACCOUNT_LOCKED NUMBER(1) DEFAULT 0,
    LOGIN_ATTEMPTS NUMBER DEFAULT 0,
    LAST_LOGIN TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. Employees Directory
CREATE TABLE HRMS_EMPLOYEES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_CODE VARCHAR2(20) UNIQUE NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    ROLE VARCHAR2(100),
    DEPARTMENT VARCHAR2(100),
    STATUS VARCHAR2(20) DEFAULT 'Active',
    EMP_TYPE VARCHAR2(20),
    EMAIL VARCHAR2(100),
    GROSS_SALARY NUMBER(10, 2) DEFAULT 0,
    DAY_RATE NUMBER(10, 2) DEFAULT 0,
    CATEGORY VARCHAR2(50),
    JOINING_DATE DATE,
    GRADE VARCHAR2(10),
    BIOMETRIC_ID NUMBER,
    UAN_NUMBER VARCHAR2(20),
    ESIC_NUMBER VARCHAR2(30),
    BANK_ACCOUNT VARCHAR2(100),
    MANAGER_ID NUMBER,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_manager FOREIGN KEY (MANAGER_ID) REFERENCES HRMS_EMPLOYEES(ID)
);

-- 3. Attendance Logs
CREATE TABLE HRMS_ATTENDANCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER NOT NULL,
    ATT_DATE DATE NOT NULL,
    PUNCH_IN VARCHAR2(10),
    PUNCH_OUT VARCHAR2(10),
    STATUS VARCHAR2(20),
    REMARKS CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_att_emp FOREIGN KEY (EMP_ID) REFERENCES HRMS_EMPLOYEES(ID),
    UNIQUE (EMP_ID, ATT_DATE)
);

-- 4. Leave Management
CREATE TABLE HRMS_LEAVE_BALANCES (
    EMP_ID NUMBER PRIMARY KEY,
    SICK NUMBER DEFAULT 0,
    CASUAL NUMBER DEFAULT 0,
    PAID NUMBER DEFAULT 0,
    CONSTRAINT fk_lb_emp FOREIGN KEY (EMP_ID) REFERENCES HRMS_EMPLOYEES(ID)
);

CREATE TABLE HRMS_LEAVE_REQUESTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER NOT NULL,
    LEAVE_TYPE VARCHAR2(50),
    START_DATE DATE,
    END_DATE DATE,
    TOTAL_DAYS NUMBER,
    STATUS VARCHAR2(20) DEFAULT 'Pending',
    REASON CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_lr_emp FOREIGN KEY (EMP_ID) REFERENCES HRMS_EMPLOYEES(ID)
);

-- 5. Payroll & Bonuses
CREATE TABLE HRMS_PAYROLL_HISTORY (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER NOT NULL,
    MONTH NUMBER NOT NULL,
    YEAR NUMBER NOT NULL,
    GROSS NUMBER(15, 2),
    BASIC NUMBER(15, 2),
    PF NUMBER(15, 2),
    ESIC NUMBER(15, 2),
    PT NUMBER(15, 2),
    TDS NUMBER(15, 2),
    ADVANCE_RECOVERY NUMBER(15, 2),
    TOTAL_DEDUCTIONS NUMBER(15, 2),
    NET_PAY NUMBER(15, 2),
    PROCESSED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_py_emp FOREIGN KEY (EMP_ID) REFERENCES HRMS_EMPLOYEES(ID)
);

CREATE TABLE HRMS_BONUS_PAYMENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_ID NUMBER NOT NULL,
    AMOUNT NUMBER(10, 2),
    MONTH NUMBER,
    YEAR NUMBER,
    BASIS NUMBER(10, 2),
    PERCENTAGE NUMBER(5, 2),
    PAID_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bp_emp FOREIGN KEY (EMP_ID) REFERENCES HRMS_EMPLOYEES(ID)
);

-- 6. Compliance & Policies
CREATE TABLE HRMS_POLICIES (
    ID VARCHAR2(50) PRIMARY KEY,
    TITLE VARCHAR2(200),
    CATEGORY VARCHAR2(100),
    UPLOAD_DATE DATE,
    EFFECTIVE_DATE DATE,
    SUMMARY CLOB,
    VERSION VARCHAR2(10),
    STATUS VARCHAR2(20),
    FILE_PATH VARCHAR2(255)
);

CREATE TABLE HRMS_POLICY_ACKS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POLICY_ID VARCHAR2(50),
    EMP_ID NUMBER,
    EMP_NAME VARCHAR2(100),
    ACK_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ack_policy FOREIGN KEY (POLICY_ID) REFERENCES HRMS_POLICIES(ID)
);

-- 7. Communication & Notices
CREATE TABLE HRMS_NOTICES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2(200),
    CONTENT CLOB,
    AUTHOR VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 8. Global Configuration (JSON Storage for flexibility)
CREATE TABLE HRMS_CONFIG (
    KEY VARCHAR2(100) PRIMARY KEY,
    VALUE CLOB CONSTRAINT ensure_json CHECK (VALUE IS JSON)
);

